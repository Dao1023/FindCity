export function countryToCode(country: string): string | null {
  const key = normalize(country);
  if (COUNTRY_MAP[key]) return COUNTRY_MAP[key];
  // Try matching by suffix
  for (const k of Object.keys(COUNTRY_MAP)) {
    if (key.endsWith(k) || key.includes(k)) return COUNTRY_MAP[k];
  }
  return null;
}
